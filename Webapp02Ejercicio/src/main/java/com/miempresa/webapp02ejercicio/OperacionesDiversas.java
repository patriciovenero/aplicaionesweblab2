public class OperacionesDiversas {
  public static double raizCuadrada(double a) {
    return Math.sqrt(a);
  }
  
  public static double raizCubica(double a) {
    return Math.cbrt(a);
  }
  
  public static double potencia(double a, double b) {
    return Math.pow(a, b);
  }
  
  public static int valorAbsoluto(int a) {
    return Math.abs(a);
  }
}
